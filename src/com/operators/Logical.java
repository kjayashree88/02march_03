package com.operators;

public class Logical {
    public static void main(String[] args) {
        int a = 7 ; int b = 9;
        //&& opertor
        System.out.println((a>b) && (a<5));
        System.out.println((a<b) && (a>5));
        System.out.println((a>b && (a<5)));
        // || opertor
        System.out.println((a>b) || (a<5));
        System.out.println((a<b) || (a>5));
        // not operator
        System.out.println(!(a>b));

    }



    }
