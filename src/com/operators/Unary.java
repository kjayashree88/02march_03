package com.operators;

public class Unary {
    public static void main(String[] args) {

        int num = 8;
        System.out.println(++num);
        System.out.println(num++);
        System.out.println(num);
        System.out.println(--num);
        System.out.println(num--);
        System.out.println(num);
    }
}
