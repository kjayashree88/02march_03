package com.patterns;

public class Letter_L {
    public static void main(String[] args) {
        int n = 5;
        for(int i=1;i<=n;i++){
            if(i<n) {
                System.out.println("*");
            }
                else{
                    for (int j=1;j<=n;j++){
                        System.out.print("*");

                }
            }
        }
    }

    }
